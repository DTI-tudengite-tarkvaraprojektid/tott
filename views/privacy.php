<h2>Andmesubjekti õigused</h2>
<p>
    Parandamisõigus – on andmesubjekti õigus nõuda, et temaga seotud ebaõiged isikuandmed põhjendamatu viivituseta
    parandataks.

    Kustutamisõigus – on andmesubjekti õigus nõuda, et tema isikuandmed põhjendamatu viivituseta kustutataks, kui on
    täidetud teatud lisatingimused.

    Teatud juhtudel on andmesubjektil õigus piirata enda isikuandmete töötlemist.

    Andmesubjektil on õigus tutvuda isikuandmetega, mida me tema kohta säilitame ja mida ta on meile andnud ning õigus
    edastada need andmed teisele vastutavale töötlejale, kui see on tehniliselt võimalik ning töötlemine toimub
    nõusoleku
    või lepingu alusel ja automatiseeritult.

    Andmesubjektil on õigus oma konkreetsest olukorrast lähtudes esitada igal ajal vastuväiteid enda isikuandmete
    töötlemise
    suhtes, mis toimub õigusaktide vastavate sätete alusel, sealhulgas nende sätete alusel toimuva profiilianalüüsi
    suhtes.

    Kui õigusaktidest ei tulene teisiti, on andmesubjektil õigus, et tema kohta ei tehtaks otsust, mis põhineb üksnes
    automatiseeritud töötlusel, sealhulgas profiilianalüüsil, ning loob andmesubjektile õiguslikke tagajärgi või avaldab
    talle märkimisväärset mõju.

    Kui isikuandmete töötlemise aluseks on andmesubjekti nõusolek, on andmesubjektil õigus see nõusolek igal ajal tagasi
    võtta, ilma et see mõjutaks enne tagasivõtmist nõusoleku alusel toimunud töötlemise seaduslikkust.

    Kui soovite kasutada nimetatud õigusi, palume võtta meiega ühendust.

    Kui MTÜ TLÜ OTT pakutud lahendus ei rahulda teid, siis on teil õigus pöörduda Eesti Vabariigi Andmekaitse
    Inspektsiooni
    ja kohtu poole.
</p>
