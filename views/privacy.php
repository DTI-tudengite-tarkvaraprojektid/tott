    klient
    on meile ise andnud oma kontaktandmed, kuid seda niikaua, kui klient pole palunud viimaste tegevuste lõpetamist või
    lahkunud uudiskirja saajate grupist.
</p>
<h3>6.1. Isikuandmete turvalisus</h3>
<p>
    Isikuandmete turvaline säilitamine on MTÜ TLÜ OTT kõrgeim turvalisuse alane prioriteet. Püüame teha kõik võimaliku,
    et
    vältida loata juurdepääsu, avaldamist ja muud õigusvastast töötlemist. Kaitseme isikuandmete konfidentsiaalsust ja
    puutumatust ning tagame andmetele juurdepääsu kooskõlas kehtivate õigusaktidega.

    Oleme meie kogutavate ja töödeldavate isikuandmete kaitseks kehtestanud mõistlikud ja piisavad organisatsioonilised
    meetmed ning tehnilised ja füüsilised piirangud. Kasutatavad meetmed sõltuvad isikuandmete liigist ja nende
    avalikukssaamise võimalikest tagajärgedest.

    MTÜ TLÜ OTT on rakendanud vajalikke tehnilisi, füüsilisi ja korralduslikke turvameetmeid kliendi isikuandmete
    (andmete)
    kaitsmiseks kadumise ja ebaseadusliku töötlemise eest.
</p>
<h3>6.2. Säilitamistähtaeg – kui kaua isikuandmeid säilitame?</h3>
<p>
    MTÜ TLÜ OTT säilitab isikuandmeid üksnes nii kaua, kui see on nende kogumise eesmärgi saavutamiseks vajalik.
    Säilitamistähtaeg sõltub ka vajadusest vastata andmesubjektide päringutele, lahendada probleeme ning täita
    seadusejärgseid dokumentide säilitamise nõudeid.

    Kui me isikuandmeid enam ei vaja ja õigusaktid nende säilitamist ei nõua, kustutame me need isikuandmed.

    Võime jätkata kõnesolevate andmete kasutamist statistilistel eesmärkidel, kuid üksnes pseudonümiseeritud või
    anonümiseeritud kujul.

    Kui tekib kasvõi väiksemgi küsimus, siis palun kirjuta support@tott.shop
</p>
