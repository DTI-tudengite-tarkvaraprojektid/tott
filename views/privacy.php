<h1>Andmekaitsetingimused</h1>
<h2>Küpsiste kasutamine- ja privaatsuspoliitika teavitus</h2>
<p>
    Käesolev küpsiste kasutamise ja privaatsuspoliitika teavitus (edaspidi teavitus) sisaldab kasulikku infot selle
    kohta,
    kuidas ja milleks ettevõtte isikuandmeid (andmeid) kogub, töötleb ja jagab. Privaatsuspoliitika lähtub andmekaitse
    üldregulatsioonist (GDPR – General Data Protection Regulation).

    Privaatsuspoliitika eesmärk on kaitsta klientide privaatsust kooskõlas Eesti Vabariigi seaduste ning Euroopa Liidu
    õigusaktidega.

    „Isikuandmed“ on mis tahes teave tuvastatud või tuvastatava füüsilise isiku kohta, näiteks nimi, aadress, e-posti
    aadress ning teave meie kodulehe külastuste, sotsiaalmeedia kanalite ja meie teenuste kasutamise kohta.

    <b>Kasutades meie veebilehte, nõustute küpsiste kasutamisega ja isikuandmete töötlemise tingimustega nagu on
        kirjeldatud meie Küpsiste kasutamise- ja privaatsuspoliitikas.</b>

    Kehtiv alates 25.05.2018
</p>

<h2>1. Kelle andmeid MTÜ TLÜ OTT töötleb?</h2>
<p>
    MTÜ TLÜ OTT on isikuandmete vastutav töötleja ning küsimuste korral kirjuta support@tott.shop.

    MTÜ TLÜ OTT töötleb oma klientide ja kontaktisikute andmeid, kes on avaldanud soovi tarbida meie teenuseid või on
    varasemalt kasutanud meie teenuseid.

    Teatud juhtudel töötleme isikuandmeid, mis on kättesaadavad avalikest allikatest.
</p>
<h2>2. Kliendi isikuandmete (andmete) töötlemise eesmärgid ja õiguslikud alused</h2>
<p>
    Meie turunduskanalite vabatahtlike kasutajate ja teenuste kasutajate ning klientide (sh klientide esindajate)
    isikuandmeid töötleme me selleks, et täita nii oma lepingulisi kohustusi kui ka pakkuda potentsiaalsetele
    klientidele
    kvaliteetsemat teenust ja informatsiooni.

    Kui MTÜ TLÜ OTT töötleb isikuandmeid neil eesmärkidel, on töötlemise õiguslikuks aluseks meie õigustatud huvi tagada
    meie teenuste korrektne ja kvaliteetsem osutamine.

    MTÜ TLÜ OTT turunduskanalitest (sh koduleht) kogume me andmeid nende kasutamise ja potentsiaalsete klientide huvide
    kohta, et paremini mõista meie klientide tegelikke huve, pakkuda kvaliteetsemat informatsiooni ja täiustada kodulehe
    funktsionaalsust ning kasutajasõbralikkust.

    Sellisel juhul on töötlemise õiguslikuks aluseks meie õigustatud huvi mõista paremini klientide vajadusi ja pakkuda
    paremat teenust.

    Näiteks töötleb MTÜ TLÜ OTT andmeid kliendi ostukäitumise kohta, andmeid uudiskirjade lugemise ja avamise kohta ning
    andmeid reklaamide avamise kohta, eesmärgiga pakkuda kliendile tema eelistatud pakkumisi ja sisu.

    <b>MTÜ TLÜ OTT töötleb isikuandmeid järgmistel eesmärkidel:</b>
</p>
<ul>
    <li>MTÜ TLÜ OTT kliendibaasi loomine ja selle haldamine;</li>
    <li>kliendile uute teenuste pakkumine;</li>
    <li>MTÜ TLÜ OTT teenuste analüüsimine ja täiustamine;</li>
    <li>veebilehe haldamine;</li>
    <li>õigusaktidest tulenevate nõuete täitmine;</li>
    <li>kliendi päringutele vastamine;</li>
    <li>tunnistuste ja sertifikaatide väljastamine.</li>
</ul>
<h2>3. Kuidas andmeid kogutakse</h2>
<p>
    Üldjuhul kogume me isikuandmeid otse andmesubjektidelt ja nende nõusolekul. Lisaks sellele kasutame me automaatseid
    andmekogumisvahendeid, sealhulgas küpsiseid ja muid jälgimisvahendeid, et optimeerida meie kodulehe kasutajakogemust
    ja
    pakkuda klientidele paremaid teenuseid.

    <b>Küpsised ja pikslisildid ning nendest vabanemine</b>

    Küpsis (cookie) on väikesemahuline tekstifail, mille veebiserver saadab kasutaja veebilehitsejale ja mis
    salvestatakse
    kasutaja arvuti kõvakettale ning mis võimaldab meelde jätta kasutaja eelistused nagu kirjasuurus, suhtluskeel,
    seadmeinfo, külastusstatistika jms

    Kõik veebisirvijad on vaikimisi seatud küpsiseid lubama, kuid veebisirvija seadeid saab üldjuhul muuta selliselt, et
    veebilehitseja keeldub küpsistest täielikult, blokeerib kolmandate isikute küpsised või teavitab kasutajat igast
    saadetud küpsisest.

    Pikslisildid on väikesed veebisaidi koodilõigud, mis võimaldavad veebilehtedel küpsiseid lugeda ja paigaldada. Need
    käivituvad, kui kasutaja avab e-kirja või saabub veebisaidile, misjärel laaditakse alla kolmandate isikute küpsiseid
    või
    registreeritakse, et kasutaja on e-kirja avanud.

    MTÜ TLÜ OTT kasutab järgmiseid küpsiseid:
<p>
<ul>
    <li>Seansiküpsised (ajutised küpsised), mille eesmärk on võimaldada teenuse kasutamist.</li>
    <li>Püsiküpsised (salvestuvad kasutaja arvutisse pärast veebilehitseja sulgemist), mille eesmärk on pidada meeles
        kliendi valikuid MTÜ TLÜ OTT veebilehtedel.</li>
</ul>
<p>
    Täpsemalt öeldes kasutab MTÜ TLÜ OTT:
</p>
<ul>
    <li>analüütilisi küpsiseid, mis koguvad teavet selle kohta, kuidas veebilehte kasutatakse. Näiteks, milliseid
        sisulehti
        külastatakse kõige enam või mida külastajad veebilehelt otsivad jne. Need küpsised ei kogu teavet, mis
        võimaldaks
        veebilehe kasutajat otseselt tuvastada. Siia hulka kuuluvad nt Google Analyticsi ja Hotjari küpsised.</li>
    <li>reklaamiküpsiseid, mida aitavad pakkuda kasutaja huvidele sihitud reklaame. Näiteks tott.shop lehele on
        lisatud Facebook Pixel kood ning kui te ei soovi MTÜ TLÜ OTT veebilehe reklaami enam Facebookis näha, siis võite
        selle
        keelata. Avage https://www.facebook.com/ads/preferences ja kustutage “Reklaamijad, kellega oled suhelnud” valiku
        alt
        TLÜ OTT.</li>
    <li>kolmandate osapoolte küpsiseid, nt Youtube ja Google Maps. Asukoha kaarti ja videosid ei näidata, kui pole
        nõustunud
        privaatsustingimustega. Selleks, et see tehniliselt oleks võimalik, kasutame Avada kujunduse poolt loodud
        privacy_embeds
        küpsist.</li>
</ul>
